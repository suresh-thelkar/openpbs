/**
 * @file	enc_Sig.c
 * @brief
 * encode_DIS_SignalJob() - encode a Signal Job Batch Request
 *
 * @par Data items are:
 * 			string		scheduler name
 */

#include <pbs_config.h>   /* the master config generated by configure */

#include "libpbs.h"
#include "pbs_error.h"
#include "dis.h"

/**
 * @brief
 *	-encode a SchdCycleEnd Batch Request
 *
 * @param[in] sock - socket descriptor
 * @param[in] scname - scheduler name
 *
 * @return      int
 * @retval      DIS_SUCCESS(0)  success
 * @retval      error code      error
 *
 */

int
encode_DIS_SchedCycleEnd(int sock, char *scname, int start_or_end)
{
	int   rc;

	if ((rc = diswst(sock, scname) != 0) ||
		 (rc = diswsi(sock, start_or_end) != 0))
			return rc;

	return 0;
}
